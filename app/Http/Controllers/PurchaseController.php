    {

        $purchase = Purchase::where('id', $id)->paginate(50);

        //合計
        $totalPurchases = Purchase::groupBy('id')
        ->where('id', $id)
        ->selectRaw('id, sum(subtotal) as total, reservation_name, time')
        ->paginate(50);

        return Inertia::render('Purchase/Show', [
            'totalPurchases' => $totalPurchases,
            'purchase' => $purchase
        ]);
    }

